NEXT = {'水':'木', '木':'火', '火':'金', '金':'水'}

def row_to_term(row):
    keys = ['id','name','phase','element','flow','position','cognition','verb']
    return dict(zip(keys, row))

def row_to_day(row):
    keys = ['id','name','element','flow','time','action','verb']
    return dict(zip(keys, row))

def primary(p):
    return p['flow'][0]

def transitional(p):
    return len(p['flow']) > 1

def relation(t, d):
    te, de = primary(t), primary(d)
    if te == de and t['element'] == d['element']:
        return '同气放大'
    if transitional(t) or transitional(d):
        if te == de:
            return '同气放大'
        if te == '火' and de == '金':
            return '炼化成器'
        if te == '金' and de == '火':
            return '结构回火'
        if {te, de} == {'水', '火'}:
            return '水火时间冲突'
        return '相位转译'
    if te == '火' and de == '金':
        return '炼化成器'
    if te == '金' and de == '火':
        return '结构回火'
    if {te, de} == {'水', '火'}:
        return '水火时间冲突'
    if te == '金' and de == '木':
        return '裁断生长'
    if te == '木' and de == '金':
        return '生长遇裁'
    if NEXT.get(te) == de:
        return '顺势推进'
    if NEXT.get(de) == te:
        return '逆势回流'
    return '并行调和'

def safe_name(text):
    return re.sub(r'[\\/:*?"<>|，。；：、\s]+', '-', text).strip('-')

