def main():
    root = Path(__file__).resolve().parents[1]
    old_144 = root / 'docs' / '144-five-element-types'
    older_144 = root / 'docs' / '144-personality-types'
    out = root / 'docs' / '288-solar-term-types'

    for path in (older_144, out):
        if path.exists():
            shutil.rmtree(path)
    # 旧 144 目录保留其 README 作为历史入口；生成新文档时不再写入旧结构。
    out.mkdir(parents=True, exist_ok=True)

    terms = [row_to_term(x) for x in TERMS]
    days = [row_to_day(x) for x in DAYS]
    index_lines = [
        '# 288 节气令认知行动类型',
        '',
        '本目录使用新版命名体系：节气令长期认知行为偏好 × 日内光热行动入口。',
        '',
        '旧版十二个月度相位与 144 类型只作为历史索引，不再作为人格类型主语。',
        ''
    ]

    for ti, t in enumerate(terms, 1):
        folder = out / f"{ti:02d}-{safe_name(t['name'])}令"
        folder.mkdir(parents=True, exist_ok=True)
        index_lines.append(f"## {ti:02d}｜{t['name']}令｜{t['phase']}")
        index_lines.append(t['cognition'])
        index_lines.append('')
        for di, d in enumerate(days, 1):
            name = type_name(t, d)
            filename = f"{di:02d}-{safe_name(t['name'])}令-x-{safe_name(d['name'])}-{safe_name(name)}.md"
            (folder / filename).write_text(make_doc(t, d), encoding='utf-8')
            index_lines.append(f"- [{name}](./{folder.name}/{filename})")
        index_lines.append('')

    (out / 'README.md').write_text('\n'.join(index_lines), encoding='utf-8')
    print(f'generated: {out}')

if __name__ == '__main__':
    main()
