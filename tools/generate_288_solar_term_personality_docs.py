def main():
    root = Path(__file__).resolve().parents[1]
    old = root / 'docs' / '144-personality-types'
    out = root / 'docs' / '288-solar-term-types'

    for path in (old, out):
        if path.exists():
            shutil.rmtree(path)
    out.mkdir(parents=True, exist_ok=True)

    terms = [row_to_term(x) for x in TERMS]
    periods = [row_to_period(x) for x in PERIODS]
    index_lines = [
        '# 288 节气光热人格底色类型',
        '',
        '本目录使用新版命名体系：节气令长期认知行为偏好 × 日内光热人格底色。',
        '',
        '类型名直接写为：节气 · 日内时段。例如：夏至·日出、秋分·日落前。',
        '',
        '日内光热时段描述人格底色中的昼夜体感层，不应被理解为行动入口。',
        ''
    ]

    for ti, t in enumerate(terms, 1):
        folder = out / f"{ti:02d}-{safe_name(t['name'])}"
        folder.mkdir(parents=True, exist_ok=True)
        index_lines.append(f"## {ti:02d}｜{t['name']}｜{t['gesture']}")
        index_lines.append(t['cognition'])
        index_lines.append('')
        for di, d in enumerate(periods, 1):
            name = type_name(t, d)
            filename = f"{di:02d}-{safe_name(name)}.md"
            (folder / filename).write_text(make_doc(t, d), encoding='utf-8')
            index_lines.append(f"- [{name}](./{folder.name}/{filename})")
        index_lines.append('')

    (out / 'README.md').write_text('\n'.join(index_lines), encoding='utf-8')
    print(f'generated: {out}')

if __name__ == '__main__':
    main()
