ORDER = {'storage': 0, 'opening': 1, 'expression': 2, 'division': 3}

def row_to_term(row):
    keys = ['id','name','gesture','family','position','cognition','verb']
    return dict(zip(keys, row))

def row_to_period(row):
    keys = ['id','name','gesture','family','time','temperament','verb']
    return dict(zip(keys, row))

def primary(p):
    return p['family'].split('-')[0]

def transitional(p):
    return '-' in p['family']

def relation(t, d):
    te, de = primary(t), primary(d)
    if te == de:
        return '同向放大'
    if transitional(t) or transitional(d):
        if te == de:
            return '同向放大'
        if te == 'expression' and de == 'division':
            return '热度成形'
        if te == 'division' and de == 'expression':
            return '标准回压'
        if {te, de} == {'storage', 'expression'}:
            return '显化回藏冲突'
        return '过渡转译'
    if te == 'expression' and de == 'division':
        return '热度成形'
    if te == 'division' and de == 'expression':
        return '标准回压'
    if {te, de} == {'storage', 'expression'}:
        return '显化回藏冲突'
    if te == 'division' and de == 'opening':
        return '裁剪生长'
    if te == 'opening' and de == 'division':
        return '生长遇界'
    if (ORDER[te] + 1) % 4 == ORDER[de]:
        return '顺势接力'
    if (ORDER[de] + 1) % 4 == ORDER[te]:
        return '逆向回流'
    return '并行调和'

def safe_name(text):
    return re.sub(r'[\\/:*?"<>|，。；：、\s]+', '-', text).strip('-')

